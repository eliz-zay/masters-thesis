# SHA256
inputs=(
    "abc"
    ""
    "abcdbcdecdefdefgefghfghighijhijkijkljklmklmnlmnomnopnopq"
)

SHA256_tmpfile=$(mktemp)

for i in "${!inputs[@]}"; do
    input="${inputs[$i]}"

    echo $input > "$SHA256_tmpfile"

    output1=$(eval ../../llvm-project/build/bin/lli $exec1 $SHA256_tmpfile)
    output2=$(eval ../../llvm-project/build/bin/lli $exec2 $SHA256_tmpfile)

    # Compare the results
    if [ "$output1" == "$output2" ]; then
        echo -e "${GREEN}* Case #$((i + 1)) - OK${NC}\n${BLUE}Input:${NC} $input\n${BLUE}Output:${NC} $output1"
    else
        echo -e "${RED}* Case #$((i + 1)) - ERROR${NC}\n${BLUE}Input:${NC} $input\n${BLUE}Expected output:${NC} $output1\n${BLUE}Received output:${NC} $output2"
    fi

    echo ""

done

rm -f "$SHA256_tmpfile"